package day04asciiwrapperclassoperatorsmemoryusage;

public class AsciiValues {
    //Java da her karakterin sayisal bir degeri vardir.
    //Bu degeler ASCII Deger'ler olarak adlandirilir.
    // Bu degelerin tamaminin bulundugu tabloya ASCII Table denir.

    public static void main(String[] args) {
      //Herhangi bir characterin ASCII degerini bulmak icin o karakterin "int" data type'nda bir variablein icinde koyunuz.

        int ch='A';

        int harf='a';
        System.out.println(ch);


        System.out.println(harf);

        int unlem='!';
        System.out.println(unlem);

        int qmark='?';
        System.out.println(qmark);




    }
}
